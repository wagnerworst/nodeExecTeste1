function soma(numeroUm,numeroDois) {
    return numeroUm+numeroDois
}

function subtracao(numeroUm, numeroDois){
    return numeroUm-numeroDois
}

function multiplicacao(numeroUm, numeroDois) {
    return numeroUm*numeroDois
}

function divisao(numeroUm, numeroDois) {
    return numeroUm/numeroDois
}

module.exports = {
    soma,
    subtracao,
    multiplicacao,
    divisao
}